export function isKarmic(n: number): boolean {
  return [13, 14, 16, 19].includes(n);
}

export function isMaster(n: number): boolean {
  return [11, 22, 33].includes(n);
}

export function numberLabel(n: number): string {
  if (isMaster(n)) return `${n} (מאסטר)`;
  if (isKarmic(n)) return `${n} (קארמה → ${n % 9 === 4 ? 4 : n % 9 === 5 ? 5 : n % 9 === 7 ? 7 : 1})`;
  return String(n);
}
